//increment and decrement operator
#include<iostream>
using namespace std;
int main()
{
    int a = 10, b = 5;
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    a++;
    cout << "After incrementing a, a = " << a << endl;
    b--;
    cout << "After decrementing b, b = " << b << endl;
    return 0;
}