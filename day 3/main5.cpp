//OPERATORS
#include<iostream>
using namespace std;
int main()
{
    int a = 10, b = 5, c;
    c = a + b;
    cout << "Addition = " << c << endl;
    c = a - b;
    cout << "Subtraction = " << c << endl;
    c = a * b;
    cout << "Multiplication = " << c << endl;
    c = a / b;
    cout << "Division = " << c << endl;
    return 0;
}